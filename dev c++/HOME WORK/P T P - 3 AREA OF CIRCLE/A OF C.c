#include <stdio.h>
int main(){
	printf("TO FIND THE AREA OF CIRCLE ");
	float radius,area;
	
	    printf("RADIUS OF THE CIRCLE= %f\n");
	    scanf("%f",&radius);
	     
	     
	// formula to find THE AREA OF CIRCLE
	area = 3.142 * radius * radius;
	
	printf("THE AREA OF CIRCLE  = %f",area);
	
	return 0;
	
}
